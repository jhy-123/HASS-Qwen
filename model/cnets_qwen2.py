    def _prepare_decoder_attention_mask(self, attention_mask, input_shape, inputs_embeds, past_key_values_length):
        # create causal mask
        # [bsz, seq_len] -> [bsz, 1, tgt_seq_len, src_seq_len]
        combined_attention_mask = None
        if input_shape[-1] > 1:
            combined_attention_mask = _make_causal_mask(
                input_shape,
                # inputs_embeds.dtype,
                torch.float32,  # [MODIFIED] force to cast to float32
                device=inputs_embeds.device,
                past_key_values_length=past_key_values_length,
            )

        if attention_mask is not None:
            # [bsz, seq_len] -> [bsz, 1, tgt_seq_len, src_seq_len]
            expanded_attn_mask = _expand_mask(attention_mask, torch.float32, tgt_len=input_shape[-1]).to(
                inputs_embeds.device
            )
            combined_attention_mask = (
                expanded_attn_mask if combined_attention_mask is None else expanded_attn_mask + combined_attention_mask
            )

        # [MODIFIED] add tree mask
        if hasattr(self, "tree_mask") and self.tree_mask is not None:
            tree_mask = self.tree_mask
            _, _, tree_shape0, tree_shape1 = tree_mask.shape
            combined_attention_mask[:, :, -tree_shape0:, -tree_shape1:][
                tree_mask == 0
                ] = torch.finfo(torch.float32).min

        return combined_attention_mask

    def forward(
        self,
        hidden_states,
        input_ids: torch.LongTensor = None,
        attention_mask: Optional[torch.Tensor] = None,
        position_ids: Optional[torch.LongTensor] = None,
        past_key_values: Optional[List[torch.FloatTensor]] = None,
        inputs_embeds: Optional[torch.FloatTensor] = None,
        use_cache: Optional[bool] = None,
        output_attentions: Optional[bool] = None,
        output_hidden_states: Optional[bool] = None,
        return_dict: Optional[bool] = None,
        q_hidden_states=None,
    ):
        batch_size, seq_length, _ = hidden_states.shape
        seq_length_with_past = seq_length
        past_key_values_length = 0

        with torch.no_grad():
            # print(f"====F.embedding====\n\
            #   masked_input: {input_ids.shape}, {input_ids}\n\
            #   self.weight: {self.embed_tokens.weight.data.shape}, {self.embed_tokens.weight.data}")
            inputs_embeds = self.embed_tokens(input_ids.to(self.embed_tokens.weight.device))
            #inputs_embeds = inputs_embeds.detach()
        # print(f"check input_embeds, {inputs_embeds.shape}, {inputs_embeds}")

        # exit(1)
        # if std is not None:
        #     noise = torch.randn(inputs_embeds.size(),device=inputs_embeds.device) * std
        #     inputs_embeds=inputs_embeds+noise

        if use_cache:
            use_legacy_cache = not isinstance(past_key_values, Cache)
            if use_legacy_cache:
                past_key_values = DynamicCache.from_legacy_cache(past_key_values)
            past_key_values_length = past_key_values.get_usable_length(seq_length)

        if position_ids is None:
            device = hidden_states.device if hidden_states is not None else inputs_embeds.device
            position_ids = torch.arange(
                past_key_values_length, seq_length + past_key_values_length, dtype=torch.long, device=device
            )
            position_ids = position_ids.unsqueeze(0).view(-1, seq_length)
        else:
            position_ids = position_ids.view(-1, seq_length).long()
        
        attention_mask = _prepare_4d_causal_attention_mask(attention_mask, (batch_size, seq_length), inputs_embeds, past_key_values_length, None, )

        if hasattr(self, "tree_mask") and self.tree_mask is not None:
            tree_len = self.tree_mask.shape[-1]
            # self.tree_mask.to(attention_mask.dtype)
            if attention_mask is None:
                assert seq_length == 1
                attention_mask = torch.cat([torch.zeros(1, past_key_values_length + seq_length - tree_len, dtype=self.tree_mask.dtype, device=self.tree_mask.device).view(1, 1, 1, -1), self.tree_mask], dim=-1)
                attention_mask[:, :, :, -tree_len:][self.tree_mask == 0] = torch.finfo(hidden_states.dtype).min
                attention_mask[:, :, :, -tree_len:][self.tree_mask == 1] = 0
            else:
                attention_mask[:, :, -tree_len:, -tree_len:][self.tree_mask == 0] = torch.finfo(hidden_states.dtype).min

        inputs_embeds=inputs_embeds.to(self.fc.weight.dtype).to(self.fc.weight.device)
        hidden_states = hidden_states.to(self.fc.weight.dtype).to(self.fc.weight.device)
        # print(f'hid before fc: {hidden_states}')
        # hidden_states=self.act(self.fc(torch.cat((inputs_embeds,hidden_states),dim=-1)))
        hidden_states = self.fc(torch.cat((inputs_embeds, hidden_states), dim=-1))
        if q_hidden_states is not None:
            q_hidden_states = self.fc(torch.cat((inputs_embeds[None, :, :, :].expand(q_hidden_states.shape[0], -1, -1, -1), q_hidden_states), dim=-1).to(dtype=torch.float16))
        # print(f"====check fc output====\n\
        #         input: {torch.cat((inputs_embeds, hidden_states), dim=-1).shape}, \n{torch.cat((inputs_embeds, hidden_states), dim=-1).dtype}, \n{torch.cat((inputs_embeds, hidden_states), dim=-1)}\n\
        #         weight: {self.fc.weight.data.shape}, \n{self.fc.weight.data.dtype}, \n{self.fc.weight.data}\n\
        #         bias: {self.fc.bias.data.shape}, \n{self.fc.bias.data.dtype}, \n{self.fc.bias.data}\n\
        #         fc output: {hidden_states.shape}, \n{hidden_states.dtype}, \n{hidden_states},")
        # print(f'hid after fc: {hidden_states}')
        # exit(1)
        all_hidden_states = () if output_hidden_states else None
        all_self_attns = () if output_attentions else None
        next_decoder_cache = () if use_cache else None


        for idx, decoder_layer in enumerate(self.layers):
            if output_hidden_states:
                all_hidden_states += (hidden_states,)

            if self.gradient_checkpointing and self.training:

                def create_custom_forward(module):
                    def custom_forward(*inputs):
                        # None for past_key_value
                        return module(*inputs,)

                    return custom_forward
                # print(f"====check encoder input====\n\
                #     hidden_states: {hidden_states.shape}, \n{hidden_states.dtype}, \n{hidden_states}\n\
                #     attention_mask: {attention_mask.shape}, \n{attention_mask.dtype}, \n{attention_mask}\n\
